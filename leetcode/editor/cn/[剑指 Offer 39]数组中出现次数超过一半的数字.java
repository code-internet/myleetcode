class Solution {
    public int majorityElement(int[] nums) {
        if(nums.length==1){
            return nums[0];
        }
        HashMap<Integer,Integer> map = new HashMap();
        int count = 1;
        Arrays.sort(nums);
        for(int i = 0;i<nums.length;i++){
            if(map.containsKey(nums[i])){
                count++;
                if(count>((nums.length-1)>>1)){
                    return nums[i];
                }
            }else {
                map.put(nums[i],nums[i]);
                count = 1;
            }
        }
        return -1;
    }
}
